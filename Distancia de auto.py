print("Calculo de la distancia recorredia de un auto")

# Definicion de variables
distancia = 0
velocidad = 0
tiempo = 0

#Capturar datos
velocidad = float(input("Ingresa la velocidad del auto: "))
tiempo = float(input("Ingresa el tiempo de recorrido del auto: "))

#Procesamiento de datos
distancia = velocidad * tiempo

#Muestra
print("\n La distancia recorrida de un auto es: " , distancia, "metros sobre segundos")

